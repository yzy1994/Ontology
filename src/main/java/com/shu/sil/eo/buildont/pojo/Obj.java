package com.shu.sil.eo.buildont.pojo;

public class Obj {
	private Long id;
	private String eon;//角色号
	private String etds;
	private String fnv;
	private String objType;
	private String gramRoleType;
	private String note;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getEon() {
		return eon;
	}
	public void setEon(String eon) {
		this.eon = eon;
	}
	public String getEtds() {
		return etds;
	}
	public void setEtds(String etds) {
		this.etds = etds;
	}
	public String getFnv() {
		return fnv;
	}
	public void setFnv(String fnv) {
		this.fnv = fnv;
	}
	public String getObjType() {
		return objType;
	}
	public void setObjType(String objType) {
		this.objType = objType;
	}
	public String getGramRoleType() {
		return gramRoleType;
	}
	public void setGramRoleType(String gramRoleType) {
		this.gramRoleType = gramRoleType;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
}
